import UIKit

extension UIColor {
    static func delicatePinkColor(alpha: Double = 1.0) -> UIColor {
        return UIColor(
            red: CGFloat(135) / 255.0,
            green: CGFloat(206) / 255.0,
            blue: CGFloat(250) / 255.0,
            alpha: CGFloat(alpha)
        )
    }
    static func fuchsiaColor(alpha: Double = 1.0) -> UIColor {
        return UIColor(
            red: CGFloat(0) / 255.0,
            green: CGFloat(255) / 255.0,
            blue: CGFloat(255) / 255.0,
            alpha: CGFloat(alpha)
        )
    }
    static func pinkColor(alpha: Double = 1.0) -> UIColor {
        return UIColor(
            red: CGFloat(0) / 255.0,
            green: CGFloat(100) / 255.0,
            blue: CGFloat(255) / 255.0,
            alpha: CGFloat(alpha)
        )
    }
    static func vividPinkColor(alpha: Double = 1.0) -> UIColor {
        return UIColor(
            red: CGFloat(0) / 255.0,
            green: CGFloat(128) / 255.0,
            blue: CGFloat(128) / 255.0,
            alpha: CGFloat(alpha)

        )
    }
    
}
